export function ShareButtons({ url, title }: { url: string; title: string }) {
  const [copied, setCopied] = useState(false);
  const u = encodeURIComponent(url);
  const t = encodeURIComponent(title);

  const copy = async () => {
    try {
      await navigator.clipboard.writeText(url);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // clipboard unavailable — no-op
    }
  };

  return (
    <div className="share reveal" data-d="2">
      <span className="share-label">Share</span>
      <a className="share-btn" href={`https://www.facebook.com/sharer/sharer.php?u=${u}`} target="_blank" rel="noopener noreferrer" aria-label="Share on Facebook">{ICONS.facebook}</a>
      <a className="share-btn" href={`https://www.linkedin.com/sharing/share-offsite/?url=${u}`} target="_blank" rel="noopener noreferrer" aria-label="Share on LinkedIn">{ICONS.linkedin}</a>
      <a className="share-btn" href={`https://twitter.com/intent/tweet?url=${u}&text=${t}`} target="_blank" rel="noopener noreferrer" aria-label="Share on X">{ICONS.x}</a>
      <a className="share-btn" href={`https://wa.me/?text=${t}%20${u}`} target="_blank" rel="noopener noreferrer" aria-label="Share on WhatsApp">{ICONS.whatsapp}</a>
      <a className="share-btn" href={`mailto:?subject=${t}&body=${u}`} aria-label="Share via email">{ICONS.email}</a>
      <button type="button" className={`share-btn${copied ? ' is-copied' : ''}`} onClick={copy} aria-label={copied ? 'Link copied' : 'Copy link'}>
        {copied ? ICONS.check : ICONS.link}
      </button>
      {copied && <span className="share-copied" role="status">Copied!</span>}
    </div>
  );
}
